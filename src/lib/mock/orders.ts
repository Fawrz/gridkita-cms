export function ordersByClient(clientId: string): Order[] {
  return orders.filter((o) => o.clientId === clientId);
}

export function ordersByDesigner(designerId: string): Order[] {
  return orders.filter((o) => o.designerId === designerId);
}

export function orderById(id: string): Order | undefined {
  return orders.find((o) => o.id === id);
}

export function historyByOrder(orderId: string): OrderStatusHistory[] {
  return orderHistory
    .filter((h) => h.orderId === orderId)
    .sort((a, b) => a.changedAt.localeCompare(b.changedAt));
}

export function paymentByOrder(orderId: string): Payment | undefined {
  return payments.find((p) => p.orderId === orderId);
}

export function deliverablesByOrder(orderId: string): Deliverable[] {
  return deliverables.filter((d) => d.orderId === orderId);
}
